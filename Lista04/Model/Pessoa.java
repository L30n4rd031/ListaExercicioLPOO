package Model;

/**
 *
 * @author aluno
 */
public class Pessoa {

    private String nome;
    private float altura;
    private String sexo;

    public Pessoa(String nome, float altura, String sexo) {
        this.nome = nome;
        this.altura = altura;
        this.sexo = sexo;
    }
    
   public Pessoa(){
       
   }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public float getAltura() {
        return altura;
    }

    public void setAltura(float altura) {
        this.altura = altura;
    }

    public String getSexo() {
        return sexo;
    }

    public void setSexo(String sexo) {
        this.sexo = sexo;
    }

}
